x=int(input("Enter a number so that I can tell you if it is odd or even:"))
#Floor Div
if x/2==x//2:
    print(x/2,x//2)
    print("This is even, I can confirm, yes.")
elif x/2!=x//2:
    print("This is odd, I can confirm, yes.")

#Modulus:
if x%2==0:
    print("This is even, I can confirm, yes.")
elif x%2!=0:
    print("This is odd, I can confirm, yes.")


